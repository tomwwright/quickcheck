import {
  attribute,
  autoGeneratedHashKey,
  table
} from "@aws/dynamodb-data-mapper-annotations";

import { Request, Notification, Result as ResultType } from "quickcheck";

@table("Results")
export class Result implements ResultType {
  @autoGeneratedHashKey()
  resultId: string;

  @attribute()
  checkId: string;

  @attribute()
  request: Request;

  @attribute()
  datetime: string;

  @attribute()
  elapsedMillis: number;

  @attribute()
  statusCode: number;

  @attribute()
  responseHeaders: { [key: string]: string };

  @attribute()
  body: string;

  @attribute()
  message: string;

  @attribute()
  sentNotifications: Notification[];
}